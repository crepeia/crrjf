                                <h2>Dados Mundiais</h2>
                                <p>O consumo abusivo de álcool, tabaco e drogas ilícitas é prevalente em todo o mundo e está entre os 20 maiores fatores de risco para problemas de saúde identificados pela Organização Mundial de Saúde (HUMENIUK; POZNYAK, 2004). Segundo relatório das Nações Unidas, quando somados os valores para uso de cannabis, estimulantes (anfetaminas ou êxtases), cocaína, opiáceos e heroína, a prevalência anual ultrapassa 200 milhões de pessoas, sendo que, aproximadamente 4,9 milhões seguem tratamentos por consumo de drogas (WHO, 2008).</p>
                                <p>Em relação ao abuso de álcool, a droga lícita de maior impacto para a saúde pública, destaca-se a relação direta do abuso da substância com diversos problemas físicos, sociais e psicológicos, tal como câncer, condições neuropsiquiátricas, cardiológicas, gastrintestinais, acidentes, suicídio, entre outros. Ressalta-se também os diversos outros efeitos diretos em grupos sociais ou sociedades, tal como absenteísmos, desempregos, problemas com justiça criminal e danos causados por crimes e acidentes automobilísticos (WHO, 2007).</p>
                                <h2>Dados Nacionais</h2>
                                <p>Segundo dados apresentados pelo II Levantamento Domiciliar sobre o uso de drogas psicotrópicas envolvendo as 108 maiores cidades brasileiras, aproximadamente 23% da população entrevistada já fez uso de qualquer droga ilícita na vida. Em relação às diversas complicações relacionadas ao abuso de substâncias, cerca de 1029 dos respondentes relataram ter vivenciado problemas no trânsito decorrentes do efeito de drogas, 606 já tiveram intercorrências  no ambientes de trabalho e 1188 afirmaram ter se ferido sob efeito da substâncias psicotrópicas (GALDURÓZ; CARLINI, 2006). </p>
                                <p>Os dados referentes ao Crack, droga com alto potencial prejudicial em curto intervalo de tempo, destacam um aumento do consumo nos últimos anos no Brasil. Estima-se que em 2001 0,4% da população já havia feito uso na vida de Crack e em 2005 este valor aumentou para 0,7%.   A faixa etária de “25 a 34 anos do sexo masculino” apresentou consumo mais prevalente tanto em 2001 quanto em 2005, entretanto o índice de prevalência subiu de 0,7 para 1,5 no último estudo (DUARTE; STEMPLIUK; BARROSO, 2009). </p>
                                <p>Outro levantamento realizado em 2003 com crianças e adolescentes em situação de rua, de 27 capitais brasileiras, confirma a disponibilidade e o consumo de derivados de cocaína no Brasil. O consumo de Crack foi mencionado em 22 capitais e os maiores índices de uso recente (uso no mês) desta substância foram observados em São Paulo, Recife e Curitiba (entre 15  e 26%), seguidas de Natal, João Pessoa, Fortaleza, Salvador e Belo Horizonte (entre 8 e 12%) (NOTO et al, 2003).</p>
                                <p>Cabe lembrar que o aumento do consumo de derivados da coca, é uma questão que merece destaque especial, tendo em vista os diversos agravos vividos por crianças e adolescentes em situação de rua, principalmente por promover isolamento social e dificultar o estabelecimento de vínculos, fator essencial para a reinserção social destes usuários (NOTO et al, 2003). </p>
                                <p>Mencionamos ainda os dados referentes à droga mais consumida no Brasil: o álcool. De acordo com o I levantamento dos padrões de consumo de álcool na população Brasileira, estima-se 28% dos adultos e 16% dos adolescentes já consumiram bebidas alcoólicas em excesso pelo menos uma vez nos últimos 12 meses. Dos bebedores, 23% relataram ter vivenciado problemas em função do consumo excessivo, sendo as complicações físicas, familiares, sociais ou de violência citadas com maior freqüência pelos entrevistados (LARANJEIRA et al, 2007). </p>
                                <p>Estudos brasileiros, assim como os dados internacionais, colocam em evidência os diversos problemas associados consumo excessivo de álcool, que se encontram além da esfera médica assistencial, relacionam-se diretamente com diversos problemas sociais: vandalismo; desordem pública; problemas familiares (como conflitos conjugais; abuso de menores; problemas interpessoais; problemas financeiros); problemas ocupacionais, dificuldades educacionais e acidentes de trânsito (MELONI, LARANJEIRA, 2004; LARANJEIRA et al, 2007).</p>
                                <p>A prevalência e os diversos problemas associados ao abuso de crack e outras drogas inserem este tema como uma questão de saúde pública e chama atenção para a necessidade de intervenções diante desta realidade. No entanto, observa-se um despreparo dos profissionais de saúde e assistência social para atuarem frente a este tema, necessitando de capacitações que envolvam a formação das diferentes categorias profissionais para abordarem estas questões na rotina dos serviços públicos.</p>
