                                <h1> Quem somos </h1>
                                
                                
<p>O <a href="../index.php">Centro de Regional de Referência sobre Drogas da Zona da Mata (CRR-JF)</a> é um centro de referência aprovados pelo edital nº 002/2010/GSIPR/SENAD do Plano Integrado de Enfrentamento ao Crack e outras Drogas, instituído pelo decreto 7.179 de 20 de maio de 2010.</p>

<p>O CRR-JF tem como objetivos qualificar e fortalecer ações da rede de atenção a usuários de crack, álcool e outras drogas. Para isso, são disponibilizados seis cursos que visam a Formação Permanente de profissionais que trabalham diretamente com a temática de álcool e outras drogas como profissionais da saúde, assistência social, segurança e educação, bem como o fortalecimento de políticas regionais e locais na área de drogas.</p>

<p>A <a href="http://www.ufjf.br">Universidade Federal de Juiz de Fora (UFJF)</a> é a Instituição de Ensino Superior cujo corpo docente responsável pela execução do presente projeto está vinculado, tendo como núcleo de coordenação das atividades de capacitação o  <a href="http://www.ufjf.br/crepeia">Centro de Referência em Pesquisa, Intervenção e Avaliação em Álcool e Outras Drogas (CREPEIA)</a>.</p>

<p>O Centro de Referência em Pesquisa, Intervenção e Avaliação em Álcool e Outras Drogas (CREPEIA) é um grupo ligado ao Pólo de Pesquisa em Psicologia Social e Saúde Coletiva (POPSS) da UFJF, composto por docentes, pesquisadores, pós-graduandos e acadêmicos com formação na área de álcool, tabaco e outras drogas. Desde 2004, vem desenvolvendo atividades acadêmicas e profissionais realizando serviços e pesquisas com diversas entidades e universidade brasileiras e estrangeiras, públicas e privadas.

<p>Uma das principais ações do grupo tem sido a capacitação de profissionais de saúde, assistência social, segurança, educação, conselheiros e gestores municipais para a implementação de ações de prevenção ao uso de álcool e outras drogas. No período de 2003 a 2010 o CREPEIA, vinculado à Universidade Federal de Juiz de Fora e às secretarias de saúde locais e regionais, capacitou aproximadamente 900 profissionais, distribuídos entre 10 diferentes municípios:</p> 
